#tipos de dados

#int
num1 = 10
print(num1)
print(type(num1))  # <class 'int'>
    
#float
num2 = 10.5
print(num2)
print(type(num2))  # <class 'float'>

#bool
num3 = True
print(num3)
print(type(num3))  # <class 'bool'>

#str
str1 = "Hello, World!"
print(str1)
print(type(str1))  # <class 'str'>

#constantes
PI = 3.14
print(PI)
print(type(PI))  # <class 'float'>

